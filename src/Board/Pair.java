package Board;

/**
 *
 * @author nehalpatel
 */
public class Pair {
    private Integer x;
    private Integer y;
    
    public Pair(Integer x, Integer y)
    {
        this.x = x;
        this.y = y;
    }
    
    public Integer getX()
    {
        return this.x;
    }
    
    public Integer getY()
    {
        return this.y;
    }
}
